function toggleButtonOne(boo) {
  const openButtonOne = document.getElementById("open-one");
  const closeButtonOne = document.getElementById("close-one");
  if (boo) {
    openButtonOne.style.display = "block";
    closeButtonOne.style.display = "none";
    return;
  }
  openButtonOne.style.display = "none";
  closeButtonOne.style.display = "block";
}

